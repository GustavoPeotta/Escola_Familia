    private void botao_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_entrarActionPerformed
        String usuario_user, usuario_senha;

        usuario_user = txt_user.getText();
        usuario_senha = txt_password.getText();

        if (db.connect()) {

            System.out.println("Conectado!");
            System.out.println("Rodando uma query contra o banco");

            query = "SELECT * FROM COORDENADORES WHERE USR = '" + usuario_user + "' AND PWD = '" + usuario_senha + "'";
            System.out.println(query);
            ResultSet rs = db.executar(query);

            try {
                if (rs != null) {
                    // Verifica se a query retornou algo
                    while (rs.next()) {
                        if ((usuario_user.equals(rs.getString(1))) && (usuario_senha.equals(rs.getString(2)))) {
                            System.out.println("Entrou");
                            user_validate = 1;
                        } else {
                            System.out.println("n√£o Entrou");
                            user_validate = 0;
                        }
                    }
                } else {
                    user_validate = 0;
                }

                if (user_validate == 1) {
                    dispose();
                    Inicio i = new Inicio();
                    i.Inicio();
                } else {
                    JOptionPane.showMessageDialog(null, "User not validate !");
                    user_validate = 0;
                }

            } catch (SQLException e) {
                e.printStackTrace();
            }

        };


    }//GEN-LAST:event_botao_entrarActionPerformed

